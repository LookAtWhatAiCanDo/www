#!/usr/bin/env node
/**
 * fetch-projects.js
 * Run by GitHub Actions on a schedule.
 * Fetches all public repos from the LookAtWhatAiCanDo org
 * and writes public/data/projects.json for the static site.
 */

const fs = require('fs');
const path = require('path');

const ORG = 'LookAtWhatAiCanDo';
const TOKEN = process.env.GITHUB_TOKEN;
const OUTPUT = path.join(__dirname, '..', 'public', 'data', 'projects.json');

// Repos to always exclude (forks, meta repos, etc.)
const EXCLUDE = [];

// Optional: pin these repos to the top regardless of sort
const PINNED = [
  'Shaide',
  'Phraims',
  'CopilotTTS',
  'Chronicler',
  'YoutubeTranscriptFetch',
  'Codeoba',
  'Ideation'
];

async function fetchRepos() {
  const headers = {
    Accept: 'application/vnd.github+json',
    'X-GitHub-Api-Version': '2022-11-28',
    'User-Agent': 'LookAtWhatAiCanDo-site-builder'
  };

  if (TOKEN) {
    headers['Authorization'] = `Bearer ${TOKEN}`;
  } else {
    console.warn('⚠️  No GITHUB_TOKEN set — rate limits will be tight');
  }

  const url = `https://api.github.com/orgs/${ORG}/repos?per_page=100&sort=updated&type=public`;
  console.log(`→ Fetching repos for org: ${ORG}`);

  const res = await fetch(url, { headers });

  if (!res.ok) {
    const body = await res.text();
    throw new Error(`GitHub API error ${res.status}: ${body}`);
  }

  const repos = await res.json();

  const projects = repos
    .filter(r => !r.private && !r.fork && !EXCLUDE.includes(r.name))
    .map(r => ({
      name: r.name,
      description: r.description || null,
      url: r.html_url,
      homepage: r.homepage || null,
      language: r.language || null,
      stars: r.stargazers_count,
      forks: r.forks_count,
      watchers: r.watchers_count,
      topics: r.topics || [],
      updatedAt: r.updated_at,
      createdAt: r.created_at,
      openIssues: r.open_issues_count
    }))
    .sort((a, b) => {
      // Pinned repos first
      const aPinned = PINNED.indexOf(a.name);
      const bPinned = PINNED.indexOf(b.name);
      if (aPinned !== -1 && bPinned !== -1) return aPinned - bPinned;
      if (aPinned !== -1) return -1;
      if (bPinned !== -1) return 1;
      // Then by most recently updated
      return new Date(b.updatedAt) - new Date(a.updatedAt);
    });

  const output = {
    _note: "This file is auto-generated by GitHub Actions. Run: node scripts/fetch-projects.js",
    _fetchedAt: new Date().toISOString(),
    _org: ORG,
    projects
  };

  fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
  fs.writeFileSync(OUTPUT, JSON.stringify(output, null, 2));

  console.log(`✅ Wrote ${projects.length} projects to ${OUTPUT}`);
  console.log(`   Projects: ${projects.map(p => p.name).join(', ')}`);
}

fetchRepos().catch(err => {
  console.error('❌ Failed to fetch projects:', err.message);
  process.exit(1);
});
